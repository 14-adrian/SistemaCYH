    private void btnPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPedidosMouseClicked
        ConexionDB verify = new ConexionDB();
        Connection con = verify.getConnection();

        LoginPP obj = new LoginPP();
        if (con != null) {
            obj.setText("La conexion a la base de datos esta funcionando correctamente!!");
            obj.setTitle("Informacion");
            GlassPanePopup.showPopup(obj);
        } else {
            obj.setText("No hay conexion a la base de datos, verifique las credenciales eh intentelo de nuevo!!");
            obj.setTitle("Advertencia");
            GlassPanePopup.showPopup(obj);
        }


    }//GEN-LAST:event_btnPedidosMouseClicked

